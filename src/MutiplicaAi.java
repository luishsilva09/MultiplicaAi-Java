public class MutiplicaAi {

    //criando o metodo da classe
    public static void mutiplicaAi(int numero){
        System.out.println("Tabela mutiplição de " + numero);

        for(int i = 1; i < 11; i++){
            int res = i * numero;
            System.out.println(numero+ " X " + i + " = " + res);
        }}
}
